import React, { useState } from 'react';
import { Link, useNavigate } from 'react-router-dom';
import { Mail, Lock, Eye, EyeOff } from 'lucide-react';
import { Button } from '../../components/Button/Button';
import { Input } from '../../components/Input/Input';
import { supabase } from '../../lib/supabase';
import styles from './Auth.module.css';

export default function LoginPage() {
    const navigate = useNavigate();
    const [email, setEmail] = useState('');
    const [password, setPassword] = useState('');
    const [showPw, setShowPw] = useState(false);
    const [error, setError] = useState('');
    const [loading, setLoading] = useState(false);
    const [googleLoading, setGoogleLoading] = useState(false);

    const handleLogin = async (e) => {
        e.preventDefault();
        setError('');
        setLoading(true);

        const { error: err } = await supabase.auth.signInWithPassword({ email, password });
        if (err) {
            setError(err.message);
        } else {
            navigate('/');
        }
        setLoading(false);
    };

    const handleGoogle = async () => {
        setGoogleLoading(true);
        const { error: err } = await supabase.auth.signInWithOAuth({
            provider: 'google',
            options: { redirectTo: `${window.location.origin}/` },
        });
        if (err) setError(err.message);
        setGoogleLoading(false);
    };

    return (
        <div className={styles.page}>
            <div className={styles.card}>
                {/* Logo */}
                <div className={styles.logoRow}>
                    <div className={styles.logoMark}>S</div>
                    <span className={styles.logoText}>Studify</span>
                </div>

                <h1 className={styles.heading}>Welcome back</h1>
                <p className={styles.subheading}>Sign in to continue to Studify</p>

                {error && <div className={styles.errorBanner}>{error}</div>}

                <form onSubmit={handleLogin} className={styles.form}>
                    <Input
                        label="Email"
                        type="email"
                        id="email"
                        placeholder="you@university.edu"
                        value={email}
                        onChange={(e) => setEmail(e.target.value)}
                        leftIcon={<Mail size={15} />}
                        required
                        autoComplete="email"
                    />
                    <Input
                        label="Password"
                        type={showPw ? 'text' : 'password'}
                        id="password"
                        placeholder="••••••••"
                        value={password}
                        onChange={(e) => setPassword(e.target.value)}
                        leftIcon={<Lock size={15} />}
                        rightIcon={
                            <button
                                type="button"
                                onClick={() => setShowPw((p) => !p)}
                                style={{ color: 'var(--color-text-muted)', background: 'none', border: 'none', cursor: 'pointer', display: 'flex' }}
                            >
                                {showPw ? <EyeOff size={15} /> : <Eye size={15} />}
                            </button>
                        }
                        required
                        autoComplete="current-password"
                    />

                    <div className={styles.forgotRow}>
                        <Link to="/forgot-password" className={styles.forgotLink}>Forgot password?</Link>
                    </div>

                    <Button type="submit" fullWidth isLoading={loading}>
                        Sign In
                    </Button>
                </form>

                <div className={styles.divider}>
                    <span>or</span>
                </div>

                <Button
                    type="button"
                    variant="secondary"
                    fullWidth
                    isLoading={googleLoading}
                    onClick={handleGoogle}
                    leftIcon={
                        <svg width="16" height="16" viewBox="0 0 24 24" xmlns="http://www.w3.org/2000/svg">
                            <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4" />
                            <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853" />
                            <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05" />
                            <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335" />
                        </svg>
                    }
                >
                    Continue with Google
                </Button>

                <p className={styles.switchText}>
                    Don't have an account?{' '}
                    <Link to="/signup" className={styles.switchLink}>Sign up</Link>
                </p>
            </div>
        </div>
    );
}
